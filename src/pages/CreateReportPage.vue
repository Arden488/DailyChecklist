<template>
  <div>
    {{passedReportForm.fields.length}}
    <div style="max-width: 700px" v-if="passedReportForm.fields.length">
      <el-alert
        v-if="editing"
        title="Editing existing record"
        type="info"
        description="You are not creating a new record, but editing one, because the record for today is already existing."
        show-icon
        style="margin-bottom: 30px;"
      >
      </el-alert>

      <ReportForm :passedReportForm="passedReportForm" />
    </div>
  </div>
</template>

<script>
import ReportsService from '@/services/ReportsService';
import QuestionsService from '@/services/QuestionsService';

import ReportForm from '@/components/ReportForm/ReportForm.vue';

export default {
  name: 'CreateReportPage',
  components: {
    ReportForm,
  },
  data() {
    return {
      passedReportForm: {
        fields: [],
      },
      editing: false
    };
  },
  methods: {
    translateIntToTime(int) {
      let s = int + '';
      s = s.length === 1 ? '0' + s : s;
      return `${s}:00`;
    },

    async getQuestions () {
      const response = await QuestionsService.fetchQuestions()
      response.data.items.forEach(el => {
        if(el.type === 'time') el.value = this.translateIntToTime(el.value);
        if(el.type === 'mood') el.value = +el.value;

        this.passedReportForm.fields.push({
          label: el.title,
          value: el.value || false,
          type: el.type,
          options: el.options
        });
      });

      this.getReport(new Date());
    },

    async getReport (date) {
      const year = date.getFullYear();
      const month = ('0' + (date.getMonth() + 1)).slice(-2);
      const dateNum = date.getDate();
      const response = await ReportsService.getReportByDate(year, month, dateNum);
      
      if (response.data.length > 0) {
        this.passedReportForm.id = response.data[0]._id;

        this.passedReportForm.fields.forEach((field, i) => {
          const eq = response.data[0].answers[i];
          if (field.type === 'boolean') {
            eq.value = !!(eq.value == 'true');
          } else if (field.type === 'mood') {
            eq.value = +eq.value;
          }

          field.value = eq.value;

        this.editing = true;
        });
      }
    }
  },
  mounted() {
    this.getQuestions();
  }
}
</script>

